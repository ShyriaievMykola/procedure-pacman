    def print_grid(self):
        for row in self.grid:
            for cell in row:
                if cell == WALL:
                    print("█", end="")
                elif cell == EMPTY:
                    print(" ", end="")
                elif cell == PACMAN:
                    print("●", end="")
                elif cell == PELLET:
                    print("·", end="")
                elif cell == GHOST_HOUSE:
                    print("#", end="")
                else:
                    print("?", end="")
            print()

    def fill_with_pellets(self):
        # Заповнює всі тунелі таблетками
        for y in range(self.height):
            for x in range(self.width):
                if self.grid[y][x] == EMPTY:
                    self.grid[y][x] = PELLET